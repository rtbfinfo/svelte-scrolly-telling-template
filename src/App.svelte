      <h2>L'idée</h2>
      <p>Il faut remonter deux ans en arrière pour voir les prémices de
      ce hackathon.</p>
      <p>A l'époque à la RTBF on commençait à réaliser des articles
      « long format » des articles de fond avec beaucoup de
      contenu, d'images et on essayait d'y intégrer des éléments
      interactifs.</p>
      <p>Par le passé nous en avions fait quelques uns avec l'équipe
      UX, les webmasters et les développeurs, mais un article qui prends
      une semaine aux équipe techniques c'était un petit peu lourd.</p>
      <p>Donc les journalistes avaient commencé à s'intéresser et à
      se former en ligne à L'HTML d'abord et également au traitement
      des données. L'objectif était d'être autonome. Ensuite ils en
      sont rapidement arrivé à devoir coder en python pour traiter les
      données et en javascript pour l'interface.</p>
      <p>Avec les limitations imparties par le site sur lequel ils
      déposaient leurs articles ils ont du trouver de plus en plus
      d'astuce pour faire fonctionner ce qu'ils arrivaient à faire
      chez eux sur le site de la RTBF.</p>
      <p>On en était là et c'est ici qu'en discutant avec les
      journalistes et en analysant la presse anglo-saxonne nous nous sommes
      dit qu'un nouveau type de journaliste était nécessaire. Une sorte
      de data journaliste capable de tout faire.</p>
      <p>Est-ce que ça existe ? Est-ce que les écoles forment à ce
      type de profile ?</p>
      <p>Peut-être que ce serait bien de construire sur nos acquis une
      master classe et de la partager avec les facultés de journalisme
      afin d'avoir ce genre de profils sur le marché d'ici 2 à 5 ans.</p>
      <p>Évidemment nous nous trompions pas mal. L'idée était
      séduisante mais l'application impossible.</p>  
    </Section>
    <h2>La gestation</h2>
    <p>Pour un projet comme celui-là avec beaucoup de partenaire,
    c'était indispensable d'avoir un support du plus haut niveau.
    Alain Dremière qui était en charge de l'équipe de rédaction
    info web et François Jadoule, responsable recherche et développement
    côté contenu, m'accompagnèrent chez l'administrateur général
    de la RTBF afin de présenter l'idée.</p>
    <p>Nous avons directement eu son support et ils nous a soutenu dans
    notre démarche auprès de différentes facultés.</p>
    <p>C'est à ce moment qu'on s'est rendu compte de la complexité
    de mettre en place une master class entrant dans le cadre d'un
    programme universitaire. Surtout en cours d'année.</p>
    <p>Intégrer un programme ça se prépare deux ans à l'avance. Il
    faut une base solide et les universités sont souvent d'une manière
    ou d'une autre en compétition. Alors toutes les rassembler autour
    de la table et intégrer un programme commun, en même temps, juste
    pour une semaine, c'est compliqué.</p>
    <p>Et surtout ce programme allait au-delà du Journalisme, en effet
    il s'adresse à tous les acteur de la communication mais également
    aux ingénieurs et informaticiens, aux étudiants en économie ou en
    marketing, il peut également toucher les ergonomes et les équipes
    UX, il était vachement plus large que ce que nous imaginions.</p>
    <p>De « Summer Class » on est passé à « Winter
    Class » puis à nouveau à « Spring Class » puis
    « Summer Class »… on tournait en rond. Sans aboutir. En
    imaginant quelque chose qui a de plus en plus d'impact et qui ne se
    limite même plus aux université mes également aux hautes écoles.</p>
    <p>Le projet restait un doux rêve, de plus en plus complexe.</p>
    <p>Entre temps le recrutement avançait à la RTBF et une cellule
    Decrypt était en train de voir le jour.</p>
    <p>On se rendait compte également qu'il fallait des compétences
    techniques et ergonomique directement au sein de la rédaction, déjà
    en interne les mentalités évoluaient. On s'orientait vers une
    équipe mutli-disciplinaire.</p>
    <h2>Le coup d'accélérateur</h2>
    <p>C'était important de ne plus porter le projet tout seul et de
    partir chercher de l'aide.</p>
    <p>Une première rencontre avec Damien Van Achter qui accepte de
    monter à bord du projet et d'en devenir l'orchestrateur permet
    de remettre en perspective certains éléments à commencer par le
    format. Passer de master class en hackathon.</p>
    <p>Une seconde rencontre avec Karim Douieb me permet de comprendre un
    message fort lié et un talent présent chez tous les ingénieurs,
    informaticiens, mathématicien, statisticien : la capacité de
    faire parler des données très complexe, de les rendre digestes,
    amusantes parfois, enrichissante certainement et surtout, toujours,
    les garder les plus objectives possible.</p>
    <p>Une troisième rencontre grâce à Corinne Boulangier avec la
    Haute école Albert Jacquard (HEAJ) via Isabelle Sacré et nous
